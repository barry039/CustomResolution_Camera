package com.doubleservice.customresolution_camera;

public class CustomSize {
    private int width;
    private int height;

    public CustomSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
